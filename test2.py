def print_name_in_stars(text):
    lines = [""] * 5
    for letter in text:
        patterns = patterns_dict.get(letter.upper(), [""] * 5)
        for i, pattern_line in enumerate(patterns):
            lines[i] += pattern_line + "  "

    for line in lines:
        print(line)


